# NUMBERS in Python

# ---------- Integer ----------
x = 10        # int
y = -5        # negative integer
z = 0         # zero

print(type(x))
print(type(y))
print(type(z))

# ---------- Float ----------
a = 2.5       # float
b = 3.0       # float
c = -4.7      # negative float

print(type(a))
print(type(b))
print(type(c))

# ---------- Complex ----------
m = 3 + 5j    # complex number
n = -2j       # complex number

print(type(m))
print(type(n))

# ---------- Scientific numbers ----------
# "e" indicates the power of 10

s1 = 35e3     # 35000.0
s2 = 12e-2    # 0.12

print(s1)
print(s2)
