# Simple string assignment
greeting = "Hello"
print(greeting)

# Multiline Strings using three quotes
multiline_text = """Lorem ipsum dolor sit amet,
consectetur adipiscing elit,
sed do eiusmod tempor incididunt
ut labore et dolore magna aliqua."""
print(multiline_text)

# Strings are Arrays: Get the character at position 1
a = "Hello, World!"
print(a[1])

# Check String Length
print(len(a))

# Check if a phrase is present in a string
txt = "The best things in life are free!"
print("free" in txt)
# STRINGS in Python

# ---------- Simple string assignment ----------
greeting = "Hello"
name = "Aziza"
print(greeting, name)

# ---------- Multiline string ----------
multiline_text = """Python is easy to learn.
It is powerful.
It is widely used."""
print(multiline_text)

# ---------- Strings as arrays ----------
word = "Python"
print(word[0])   # first character
print(word[3])   # fourth character

# ---------- String length ----------
text = "Hello, World!"
print(len(text))

# ---------- Check if a substring exists ----------
sentence = "Learning Python is fun!"
print("Python" in sentence)
